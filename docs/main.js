function init() {

  camera = new THREE.PerspectiveCamera(40, window.innerWidth / window.innerHeight, 1, 10000);
  camera.position.z = 3000;

  scene = new THREE.Scene();

  icons.map(icon => {
    var { hex, title, source, active, } = icon;
    var color = new THREE.Color(hex);
    var hsl = color.getHSL();
    var rgb = { r: color.r, g: color.g, b: color.b };
    var cmy = { c: 1 - rgb.r, m: 1 - rgb.g, y: 1 - rgb.b };
    var link = '#' + title.replace(/[!|’|.| |-]/g, "").toLowerCase()
    var hexString = `#${hex.toString(16).padStart(6, '0')}`
    var data = {
      hex: hexString,
      hsl: hsl,
      rgb: rgb,
      cmy: cmy,
      link: link,
      source: source,
      title: title,
      active: active,
    };

    var element = document.createElement('div');
    element.className = 'element';

    var svgEl = document.createElementNS(SVG_NS, 'svg');
    svgEl.style.color = `#${hex.toString(16).padStart(6, '0')}`;
    element.appendChild(svgEl);

    var gEl = document.createElementNS(SVG_NS, 'g');
    gEl.addEventListener('click', function (d) {
      return function (event) {
        if (selected) {
          selected.style.color = 'inherit';
        }
        selected = this.children[1];
        selected.style.color = '#ffffff';

        iconInfo.innerHTML = '';

        var iconTitle = document.createElement('div');
        iconTitle.textContent = d.title
        iconInfo.appendChild(iconTitle);

        var icon = document.createElement('div');
        info.className = 'element';
        iconInfo.appendChild(icon)

        var iconSVGEl = document.createElementNS(SVG_NS, 'svg');
        iconSVGEl.style.color = d.hex;
        icon.appendChild(iconSVGEl);

        var iconUseEl = document.createElementNS(SVG_NS, 'use');
        iconUseEl.setAttributeNS(XLink_NS, 'xlink:href', d.link);
        iconSVGEl.appendChild(iconUseEl);

        var iconHexEl = document.createElement('div');
        iconHexEl.textContent = `hex: ${d.hex}`;
        iconInfo.appendChild(iconHexEl);

        var iconHSLEl = document.createElement('div');
        iconHSLEl.textContent = `hsl: hsl(${(d.hsl.h * 360).toFixed(2)}, ${(d.hsl.s * 100).toFixed(2)}%, ${(d.hsl.l * 100).toFixed(2)}%)`;
        iconInfo.appendChild(iconHSLEl);

        var iconRGBEl = document.createElement('div');
        iconRGBEl.textContent = `rgb: rgb(${(d.rgb.r * 255).toFixed(2)}, ${(d.rgb.g * 255).toFixed(2)}, ${(d.rgb.b * 255).toFixed(2)})`;
        iconInfo.appendChild(iconRGBEl);

        var iconHexEl = document.createElement('div');
        iconHexEl.textContent = `cmy: ${(d.cmy.c).toFixed(2)}, ${(d.cmy.m).toFixed(2)}, ${(d.cmy.y).toFixed(2)}`;
        iconInfo.appendChild(iconHexEl);

        var iconWebEl = document.createElement('a');
        iconWebEl.setAttribute('target', '_blank');
        iconWebEl.setAttribute('href', d.source);
        iconWebEl.textContent = d.source;

        iconInfo.appendChild(iconWebEl);
      }
    }(data));
    svgEl.appendChild(gEl);

    var bgEl = document.createElementNS(SVG_NS, 'use');
    bgEl.setAttributeNS(XLink_NS, 'xlink:href', link);
    bgEl.style.stroke = '#000000';
    bgEl.style.strokeWidth = 0.5;
    gEl.appendChild(bgEl);

    var useEl = document.createElementNS(SVG_NS, 'use');
    useEl.setAttributeNS(XLink_NS, 'xlink:href', link);
    gEl.appendChild(useEl);

    var titleEl = document.createElementNS(SVG_NS, 'title');
    titleEl.textContent = title;
    gEl.appendChild(titleEl);

    var object = new THREE.CSS3DObject(element);
    object.position.x = 0;
    object.position.y = 0;
    object.position.z = 0;

    //

    var listEntry = document.createElement('div');
    iconList.appendChild(listEntry);

    var listInput = document.createElement('input');
    listInput.setAttribute('type', 'checkbox');
    listInput.setAttribute('id', `${link.slice(1)}-icon`);
    listInput.setAttribute('checked', true);
    listInput.addEventListener('change', function(d, el) {
      return function (event) {
        var target = event.target;
        var checked = target.checked;

        el.style.display = !!checked ? 'block' : 'none';
      }
    }(data, element));
    listEntry.appendChild(listInput);

    var listLabel = document.createElement('label');
    listLabel.setAttribute('for', `${link.slice(1)}-icon`);
    listLabel.textContent = title;
    listEntry.appendChild(listLabel);

    scene.add(object);
    objects.push(object);

    // HSL

    var objectHSL = new THREE.Object3D();

    cylindrical.set(hsl.s * 800, hsl.h * 2 * Math.PI, (hsl.l * 1600) - 800);
    objectHSL.position.setFromCylindrical(cylindrical);
    objectHSL.lookAt(camera.position);

    targets.hsl.push(objectHSL);

    // RGB

    var objectRGB = new THREE.Object3D();

    objectRGB.position.set(rgb.r * 1600 - 800, rgb.g * 1600 - 800, rgb.b * 1600 - 800);
    objectRGB.lookAt(camera.position);

    targets.rgb.push(objectRGB);

    // CMY

    var objectCMY = new THREE.Object3D();

    objectCMY.position.set(cmy.c * 1600 - 800, cmy.m * 1600 - 800, cmy.y * 1600 - 800);
    objectCMY.lookAt(camera.position);

    targets.cmy.push(objectCMY);
  });

  //

  renderer = new THREE.CSS3DRenderer();
  renderer.setSize(window.innerWidth, window.innerHeight);
  renderer.domElement.style.position = 'absolute';
  document.getElementById('container').appendChild(renderer.domElement);

  //

  controls = new THREE.OrbitControls(camera, renderer.domElement);
  controls.rotateSpeed = 0.5;
  controls.minDistance = 500;
  controls.maxDistance = 6000;
  controls.minPolarAngle = (Math.PI / 180) * 20;
  controls.maxPolarAngle = Math.PI - controls.minPolarAngle;
  controls.addEventListener('change', render);

  var btnHSL = document.getElementById('hsl');
  btnHSL.addEventListener('click', function ( event) {
    transform(targets.hsl, 2000);
  }, false );

  var btnRGB = document.getElementById('rgb');
  btnRGB.addEventListener('click', function ( event) {
    transform(targets.rgb, 2000);
  }, false );

  var btnCMY = document.getElementById('cmy');
  btnCMY.addEventListener('click', function ( event) {
    transform(targets.cmy, 2000);
  }, false );

  //

  transform(targets.hsl, 2000)

  //

  window.addEventListener('resize', onWindowResize, false);

}

function transform(targets, duration) {
  TWEEN.removeAll();

  for (var i = 0; i < objects.length; i ++) {
    var object = objects[ i ];
    var target = targets[ i ];

    var rot = target.clone();
    rot.lookAt(camera.position);

    new TWEEN.Tween(object.position)
      .to( { x: target.position.x, y: target.position.y, z: target.position.z }, Math.random() * duration + duration )
      .easing(TWEEN.Easing.Exponential.InOut)
      .start();

    new TWEEN.Tween(object.rotation)
      .to( { x: rot.rotation.x, y: rot.rotation.y, z: rot.rotation.z }, Math.random() * duration + duration )
      .easing(TWEEN.Easing.Exponential.InOut)
      .start();
  }

  new TWEEN.Tween(this)
    .to({}, duration * 2)
    .onUpdate(render)
    .start();
}

function onWindowResize() {
  camera.aspect = window.innerWidth / window.innerHeight;
  camera.updateProjectionMatrix();

  renderer.setSize(window.innerWidth, window.innerHeight);

  render();
}

function animate() {
  requestAnimationFrame(animate);

  TWEEN.update();

  controls.update();
}

function render() {
  renderer.render(scene, camera);

  objects.forEach(object => {
    if (object && object.lookAt) {
      object.lookAt(camera.position);
    }
  })
}